    public ArrayList<SetSchool> setSchoolTilte(){
        ArrayList<SetSchool> schoolAr = new ArrayList<SetSchool>();
        String sql = "SELECT * FROM set_school WHERE set_school_id ='"+setSchool()+"' ";
        Connection con = getConnections();
        Statement stm;
        ResultSet res;
        try {
            stm = con.createStatement();
            res = stm.executeQuery(sql);
            while(res.next()){
                SetSchool a = new SetSchool(res.getInt("setSchoolId"),res.getString("school_title"));
                schoolAr.add(a);
                System.out.println("values from "+res.getArray(sql));
            }
        } catch (SQLException ex) {
            Logger.getLogger(SetSchools.class.getName()).log(Level.SEVERE, null, ex);
        }
        return schoolAr;
    }
    
