    public ArrayList<SetSchool> getListOfSchool(){
        ArrayList<SetSchool> schoolList = new ArrayList<>();
        String sql = "SELECT * FROM set_school";
        Connection conn = getConnections();
        Statement stm;
        ResultSet result;
        try{
        stm = conn.createStatement();
        result = stm.executeQuery(sql);
        SetSchool school;
        while(result.next()){
            school = new SetSchool(result.getInt("set_school_id"),result.getString("school_title"));
            schoolList.add(school);
        }
        
        }catch(SQLException ex){
            Logger.getLogger(SetSchool.class.getName()).log(Level.SEVERE, null, ex);
        }
     return schoolList;   
    }
    
    
    
    
    
